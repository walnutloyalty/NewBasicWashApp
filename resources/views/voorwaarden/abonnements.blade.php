                    zullen partijen in onderling overleg voorzien in een vervangende bepaling waarbij het doel en de
                    strekking van de te vervangen bepaling zoveel mogelijk wordt behouden.</p>
                <p>Berkman Carwash B.V. is niet aansprakelijk voor elke vorm van letsel of schade aan zowel de auto als
