                <h2 class="mt-8 font-bold text-xl leading-8 text-black">Artikel 12 Gratis proefperioden</h2>
                <p>Een Abonnement kan met een gratis proefperiode beginnen. De gratis proefperiode is bedoeld om nieuwe
                    leden en bepaalde voormalige Abonnees de service uit te laten proberen.</p>
                <p>Berkman Carwash B.V. bepaalt naar eigen inzicht of je in aanmerking komt voor de gratis proefperiode
                    en kan de criteria hiervoor beperken om te voorkomen dat gratis proefperioden worden misbruikt.
                    Berkman Carwash B.V. behoudt zich het recht voor om de gratis proefperiode in te trekken als zij
                    vaststelt dat je hiervoor niet in aanmerking komt. Aan de hand van gegevens die voor een bestaand of
                    recent Abonnement worden gebruikt, zoals een kenteken, betalingsmethode of e-mailadres voor je
                    account, kan Berkman Carwash B.V. bepalen of je in aanmerking komt voor de gratis proefperiode. Er
                    kunnen beperking gelden voor combinaties met andere aanbiedingen.</p>
                <p>Berkman Carwash B.V. schrijft je maandelijkse abonnementskosten aan het einde van de gratis
                    proefperiode af via je betaalmethode, tenzij je je Abonnement voor het einde van de gratis
                    proefperiode opzegt.</p>
