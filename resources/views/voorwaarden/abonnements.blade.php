                <!--Artikel 2-->
                <h2 class="mt-8 font-bold text-xl leading-8 text-black">Artikel 2 Inhoud abonnement</h2>
                <p>Het Abonnement is voertuig en kenteken gebonden en niet overdraagbaar.</p>
                <p>Onder onbeperkt wassen wordt verstaan dat de Abonnee onbeperkt gebruik mag maken van de dienst zoals
                    overeengekomen in het Abonnement, mits er sprake is van normaal consumenten gebruik conform Artikel
                    5.2.</p>
                <p>Het abonnement geeft het voertuig van de abonnee gedurende de looptijd van het abonnement recht op
                    het autowassen bij de wasstraat met inachtneming van het bepaalde in Artikel 5.</p>
                <p>Berkman Carwash B.V. behoudt zich het recht voor een Abonnement te weigeren aan personen met wie een
                    eerder Abonnement is beëindigd conform Artikel 4.4.</p>
                <!--Artikel 3-->
                <h2 class="mt-8 font-bold text-xl leading-8 text-black">Artikel 3 Duur van het abonnement</h2>
                <p>Een maandabonnement wordt aangegaan voor een minimale abonnementsperiode van een maand, behoudens het
                    bepaalde in Artikel 4.4 tot en met 4.6.</p>
                <p>Een jaarabonnement wordt aangegaan voor een minimale abonnementsperiode van een jaar, behoudens het
                    bepaalde in Artikel 4.4 tot en met 4.6. Na het eerste jaar wordt het tweede jaar via automatische
                    incasso
                    in rekening gebracht voor hetzelfde wasprogramma.</p>
                <p>Het Abonnement zal worden opgezegd conform Artikel 4.1 tot en met 4.3.</p>
                <!--Artikel 4-->
                <h2 class="mt-8 font-bold text-xl leading-8 text-black">Artikel 4 Opzegging van het abonnement</h2>
                <p>Een maandabonnement kan te allen tijde worden opgezegd. Nadat je hebt opgezegd, kun je de dienst
                    zoals overeengekomen in het Abonnement gebruiken tot het einde van de maandelijkse
                    factureringsperiode. Voor zover toegestaan onder het toepasselijk recht, worden betalingen niet
                    gerestitueerd en bieden we geen terugbetaling of tegoed voor gedeeltelijke lidmaatschapsperioden van
                    het abonnement.</p>
                <p>Een jaarabonnement kan te allen tijde worden opgezegd. Voor zover toegestaan onder het toepasselijk
                    recht, worden betalingen niet gerestitueerd en bieden we geen terugbetaling of tegoed voor
                    gedeeltelijke lidmaatschapsperioden van het Abonnement. Als het Abonnement wordt opgezegd voor het
                    verstrijken van het eerste jaar, kun je de dienst zoals overeengekomen in het Abonnement gebruiken
                    tot 365 dagen na het afsluiten van het jaarabonnement.
                    Als het Abonnement wordt opgezegd na het verstrijken van het eerste jaar, kun je de dienst zoals
                    overeengekomen in het Abonnement gebruiken tot het einde van de factureringsperiode.</p>
                <p>Als je je Abonnement opzegt, wordt je account automatisch afgesloten aan het einde van je huidige
                    factureringsperiode. Opzeggen kan door een schrijven te richten aan: Berkman Carwash B.V., t.a.v.
                    Afdeling Abonnementen, Tuindersweg 34, 2991 LR Barendrecht. Opzeggen via e-mail wordt als
                    gelijkwaardig beschouwd, mits gericht aan <a href="mailto:info@basicwash.nl"
                                                                 class="text-pink-600 no-underline">info@basicwash.nl</a>
                    t.a.v. Afdeling Abonnementen. Enkel
                    opzeggingen met volledige gegevens worden in behandeling genomen. Binnen een week ontvangt de
                    Abonnee een bevestiging van de opzegging per e-mail.</p>
                <p>Berkman Carwash B.V. behoudt zich het recht voor het Abonnement per direct op te zeggen als de
                    Abonnee: Berkman Carwash B.V. onjuiste gegevens heeft verschaft, fraude pleegt in het gebruik van
                    het Abonnement, niet heeft voldaan aan de betalingsplicht in Artikel 7 of als Berkman Carwash B.V.
                    daarvoor een andere reden gegrond acht.</p>
                <p>Als het Abonnement op grond van Artikel 4.4 is opgezegd en de minimale abonnementsperiode is nog niet
                    verlopen, is de Abonnee gehouden om een vergoeding te betalen voor de resterende minimale
                    abonnementsperiode. De Abonnee heeft geen recht op restitutie van reeds betaald abonnementsgeld.</p>
                <p>In gevallen waarin het Abonnement op grond van Artikel 4.4 is opgezegd, behoudt Berkman Carwash B.V.
                    zich het recht voor de voormalig Abonnee te weigeren een volgend Abonnement af te sluiten.</p>
                <!--Artikel 5-->
                <h2 class="mt-8 font-bold text-xl leading-8 text-black">Artikel 5 Gebruik van het abonnement</h2>
                <p>De Abonnee heeft recht om één voertuig onbeperkt te wassen bij de Wasstraat van Berkman Carwash B.V.,
                    mits sprake is van normaal consumenten gebruik zoals gedefinieerd in Artikel 5.2.</p>
                <p>Onder normaal consumenten gebruik (fair use policy) wordt verstaan dat het gebruik van de Wasstraat
                    niet hoger mag liggen dan drie maal het gemiddelde gebruik van het desbetreffende Abonnement.</p>
                <!--Artikel 6-->
                <h2 class="mt-8 font-bold text-xl leading-8 text-black">Artikel 6 Prijzen</h2>
                <p>Berkman Carwash B.V. behoudt zich het recht voor de prijs van het Abonnement te wijzigen. Berkman
                    Carwash B.V. stelt de Abonnee hiervan minimaal één maand voordat de prijswijziging van kracht wordt
                    per e-mail op de hoogte.</p>
                <p>Alle genoemde prijzen zijn onder voorbehoud van druk- en zetfouten. Voor de gevolgen van druk- en
                    zetfouten wordt geen aansprakelijkheid aanvaard.</p>
                <p>Alle genoemde prijzen zijn in Euro’s en inclusief BTW.</p>
                <p>Restitutie van het Abonnementsgeld is niet mogelijk, ook niet bij storing, weersomstandigheden of
                    enige andere oorzaak van tijdelijke sluiting van de Wasstraat.</p>
                <!--Artikel 7-->
                <h2 class="mt-8 font-bold text-xl leading-8 text-black">Artikel 7 Betaling</h2>
                <p>Voor een maandabonnement is het abonnementsgeld maandelijks verschuldigd. Dit wordt maandelijks via
                    automatische incasso afgeschreven van de door Abonnee opgegeven bankrekening. De eerste termijn
                    dient direct via iDEAL te worden betaald. Bij een niet tijdige betaling van het abonnementsgeld is
                    Berkman Carwash B.V. genoodzaakt € 15,00 administratiekosten in rekening te brengen.</p>
                <p>Voor een jaarabonnement dient de eerste termijn direct via iDEAL te worden betaald. Dit wordt
                    jaarlijks via automatische incasso afgeschreven van de door Abonnee opgegeven bankrekening. Bij een
                    niet tijdige betaling van het abonnementsgeld is Berkman Carwash B.V. genoodzaakt € 15,00
                    administratiekosten in rekening te brengen.</p>
                <p>Door ondertekening van de abonnementsovereenkomst machtigt de Abonnee
                    Berkman Carwash B.V., dan wel een door Berkman Carwash B.V. aan te wijzen derde, tot incasso van het
                    abonnementsgeld.</p>
                <p>Maandelijkse abonnementskosten en eventuele kosten in verband met het gebruik van het Abonnement
                    worden maandelijks afgeschreven op het door Abonnee opgegeven rekeningnummer op de kalenderdag
                    waarop het betalende gedeelte van je lidmaatschap is ingegaan. In sommige gevallen kan de
                    betalingsdatum worden gewijzigd, bijvoorbeeld als Berkman Carwash B.V. een bedrag niet kon
                    afschrijven of als je betaalde lidmaatschap is ingegaan op een dag die in een bepaalde maand niet
                    voorkomt.</p>
                <p>Het is niet mogelijk de betaling van het abonnementsgeld aan de kassa van de Wasstraat te
                    voldoen.</p>
                <p>Indien de incasso, om wat voor reden dan ook, storneert, behoudt Berkman Carwash B.V. zich het recht
                    voor haar verplichtingen onder het Abonnement op te schorten en het Abonnement buiten werking te
                    stellen. Het Abonnement wordt weer in gebruik gesteld zodra de Abonnee het volledig verschuldigde
                    abonnementsgeld en/of eventuele administratiekosten heeft voldaan. De Abonnee heeft geen recht op
                    restitutie van abonnementsgeld over de periode dat het Abonnement buiten werking is geweest.</p>
                <p>Indien de Abonnee niet tijdig voldoet aan de betalingsverplichtingen voortkomend uit deze
                    voorwaarden, zal Berkman Carwash B.V. de daardoor ontstane incassokosten en eventuele rentekosten
                    verhalen op Abonnee. Tevens zal Berkman Carwash B.V. de betalingsvordering overdragen aan een door
                    haar aan te wijzen incassobureau. De kosten die Berkman Carwash B.V. redelijkerwijs zal moeten maken
                    als gevolg van deze niet nakoming van de Abonnee, komen voor rekening van de Abonnee.</p>
                <p>Op een jaarabonnement na, voldoet de Abonnee alle betalingen in enkelvoudige maandelijkse termijnen.
                    Afwijkende bedragen zullen worden teruggestort en zullen niet kwalificeren als geldende
                    betaling.</p>
                <p>Wijziging van het kenteken, bankrekeningnummer en/of te naam stelling zijn enkel mogelijk middels het
                    afsluiten van een nieuw Abonnement zoals beschreven in Artikel 8.</p>
                <!--Artikel 8-->
                <h2 class="mt-8 font-bold text-xl leading-8 text-black">Artikel 8 Wijziging abonnementsgegevens</h2>
                <p>Het bij een Abonnement opgegeven kenteken kan niet door Abonnee zelf worden gewijzigd. Abonnee kan
                    contact opnemen met de klantenservice van Berkman Carwash B.V. via info@basicwash.nl om een kenteken
                    te wijzigen.</p>
                <p>Bankrekeningnummer en te naam stelling kunnen niet worden aangepast in een
                    reeds afgesloten Abonnement.</p>
                <p>Abonnee kan zijn/haar bankrekeningnummer en/of te naam stelling wijzigen door het huidige Abonnement
                    te beëindigen, waarna een Abonnement met de nieuwe gegevens moet worden afgesloten.</p>
                <p>De Abonnee heeft geen recht op restitutie van abonnementsgeld over de periode dat het Abonnement niet
                    kan worden gebruikt.</p>
